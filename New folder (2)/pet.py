class Pet:

    def __init__(self, name, a_type, age):

    # Data attributes

        self.__name = name

        self.__a_type = a_type

        self.__age = age

    def set_name(self, name):

        #Animal name

        self.__name = name

    def set_a_type(self, a_type):

        # animal type

        self.__a_type = a_type

    def set_age(self, age):

        # animal age

        self.__age = age


    def get_name(self):

        #Animal name

        return self.__name

    def get_a_type(self):

        # animal type

        return self.__a_type

    def get_age(self):

        # animal age

        return self.__age



        

        

        
